        [Test]
        public void Multiply_NumberMultipiledBy1_ShouldBeSameNumber()
        {
            int[] first = {1, 2, 3};
            int[] second = {1};
            int[] expected = {1, 2, 3};
            BigInt.Multiply(first, second).Should().BeEquivalentTo(expected);
        }

        [Test]
        public void Multiply_SameNumber_ShouldBeEqual()
        {
            int[] first = {1, 2, 3};
            int[] second = {1, 2, 3};
            int[] expected = {1, 4, 0, 3, 0, 1};
            BigInt.Multiply(first, second).Should().BeEquivalentTo(expected);
        }

